local debounce = false

X.MouseButton1Click:Connect(function()
	if debounce == false then
		debounce = true
		Main.Position = UDim2.new(0.382268965, 0, 1.18099999, 0)
		openutg.Visible = true
		wait(0.5)
		debounce = false
	end
end)

openutg.MouseButton1Click:Connect(function()
	if debounce == false then
		debounce = true
		Main.Position = UDim2.new(0.382268965, 0, 0.235, 0)
		openutg.Visible = false
		wait(0.5)
		debounce = false
	end
end)

for i, v in pairs(ScrollingFrame:GetChildren()) do
	if v:IsA("TextButton") then
		v.MouseButton1Click:Connect(function()
			if game.Players.LocalPlayer.Character then
				local name = v.Name
				game:GetService("VirtualInputManager"):SendKeyEvent(true, Enum.KeyCode.E, false, nil)
				wait(0.05)
				game:GetService("VirtualInputManager"):SendKeyEvent(false, Enum.KeyCode.E, false, nil)
				wait(0.1)
				game:GetService("ReplicatedStorage").Remotes.Troll:FireServer(name)
			else
				game.Players.LocalPlayer:LoadCharacter()
				wait(1)
				local name = v.Name
				game:GetService("VirtualInputManager"):SendKeyEvent(true, Enum.KeyCode.E, false, nil)
				wait(0.05)
				game:GetService("VirtualInputManager"):SendKeyEvent(false, Enum.KeyCode.E, false, nil)
				wait(0.1)
				game:GetService("ReplicatedStorage").Remotes.Troll:FireServer(name)
			end
		end)
	end
end
